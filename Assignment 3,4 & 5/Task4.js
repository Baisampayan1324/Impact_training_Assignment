// Task 4: Declare Two Variables and Perform Operations

let a = 10, b = 5;

// Arithmetic Operations
console.log("Sum:", a + b);
console.log("Difference:", a - b);
console.log("Multiplication:", a * b);
console.log("Division:", a / b);
console.log("Modulus:", a % b);

// Bitwise Operations
console.log("Bitwise AND:", a & b);
console.log("Bitwise OR:", a | b);
console.log("Bitwise XOR:", a ^ b);

// Relational Operations
console.log("Is a greater than b?", a > b);
console.log("Is a equal to b?", a == b);

// Logical Operations
console.log("Logical AND:", a > 0 && b > 0);
console.log("Logical OR:", a > 0 || b < 0);
console.log("Logical NOT:", !(a > 0));

// Assignment Operations
a += 5; // a = a + 5
console.log("Updated value of a:", a);
